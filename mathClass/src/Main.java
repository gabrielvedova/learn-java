
public class Main {
    public static void main(String[] args) {

        //System.out.println(Math.PI);
        //System.out.println(Math.E);

        double result;

        result = Math.pow(2, 3); // 2^3
        System.out.println("2^3 = " + result);

        result = Math.abs(-5); // Absolute value of -5
        System.out.println("|-5| = " + result);

        result = Math.sqrt(16); // Square root of 16
        System.out.println("√16 = " + result);

        result = Math.round(3.14); // Round 3.14 to the nearest integer
        System.out.println("Arredondamento 3.14 = " + result);

        result = Math.ceil(3.14); // Round up 3.14
        System.out.println("Arrdondamento para cima 3.14 = " + result);

        result = Math.max(10, 20); // Maximum of 10 and 20
        System.out.println("Máximo de 10 e 20 = " + result);
    }
}
